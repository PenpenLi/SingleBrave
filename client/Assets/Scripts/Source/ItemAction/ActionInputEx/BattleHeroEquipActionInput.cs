/// <summary>
/// 战斗英雄装备Action输入类
/// </summary>
public class BattleHeroEquipActionInput : ActionInput
{
    private BATTLE_TIME_SCENE m_eTimeScene;  //时间场景
    private BattleHero m_cHero; //英雄

    public BattleHeroEquipActionInput()
        : base()
    { 
        //
    }

    /// <summary>
    /// 获取时间场景
    /// </summary>
    /// <returns></returns>
    public BATTLE_TIME_SCENE GetTimeScene()
    {
        return this.m_eTimeScene;
    }

    /// <summary>
    /// 设置时间场景
    /// </summary>
    /// <param name="timeScene"></param>
    public void SetTimeScene(BATTLE_TIME_SCENE timeScene)
    {
        this.m_eTimeScene = timeScene;
    }

    /// <summary>
    /// 获取战斗英雄
    /// </summary>
    /// <returns></returns>
    public BattleHero GetBattleHero()
    {
        return this.m_cHero;
    }

    /// <summary>
    /// 设置战斗英雄
    /// </summary>
    /// <param name="hero"></param>
    public void SetBattleHero(BattleHero hero)
    {
        this.m_cHero = hero;
    }
}

/// <summary>
/// 英雄事件输入参数
/// </summary>
public class HeroActionInput : ActionInput
{
    private Hero m_cHero;   //英雄

    public HeroActionInput()
    { 
        //
    }

    /// <summary>
    /// 设置英雄
    /// </summary>
    /// <param name="hero"></param>
    public void SetHero(Hero hero)
    {
        this.m_cHero = hero;
    }

    /// <summary>
    /// 获取英雄
    /// </summary>
    /// <returns></returns>
    public Hero GetHero()
    {
        return this.m_cHero;
    }
}

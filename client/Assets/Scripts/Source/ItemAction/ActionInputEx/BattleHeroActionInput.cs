/// <summary>
/// 战场英雄Action输入类
/// </summary>
public class BattleHeroActionInput : ActionInput
{
    private BattleHero m_cBattleHero;   //战场英雄
    private BattleHero[] m_vecBattleHero;   //战场所有英雄

    public BattleHeroActionInput()
    { 
        //
    }

    /// <summary>
    /// 设置战场英雄
    /// </summary>
    /// <param name="hero"></param>
    public void SetBattleHero(BattleHero hero)
    {
        this.m_cBattleHero = hero;
    }

    /// <summary>
    /// 获取战场英雄
    /// </summary>
    /// <returns></returns>
    public BattleHero GetBattleHero()
    {
        return this.m_cBattleHero;
    }

    /// <summary>
    /// 设置所有英雄
    /// </summary>
    /// <param name="vec"></param>
    public void SetBattleAllHero(BattleHero[] vec)
    {
        this.m_vecBattleHero = vec;
    }

    /// <summary>
    /// 获取所有英雄
    /// </summary>
    /// <returns></returns>
    public BattleHero[] GetBattleAllHero()
    {
        return this.m_vecBattleHero;
    }
}
